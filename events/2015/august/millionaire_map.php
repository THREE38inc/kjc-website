			<p align=justify>The Millionaire's Map is the first book in history to integrate this mysterious code into an easy method for blueprinting your future and creating the life you desire.</p>

			<p align=justify>The School of management of Kristu Jayanti College, had organized the International Lecture Series on Millionaire Map on 7th august 2015 to inculcate and train the students on developing the ability to perceive. The program commenced with the lighting of the lamp invoking the presence of the almighty God. During the session, the students were given brief knowledge about the power of being rich and being a millionaire. Mr. Albert Charles Vincent, CEO of GLUECK Technologies Malaysia was invited to take over the Session. His life experiences and examples on Life values, on disciplines, on philanthropist relationship did bring an entire different outlook on our perspective of life. In Address to the students he told the students to dream high, believing on one's self having the attitude of never giving up, achieving greater things along with great purposes focusing on the key aspects such as discipline, ability to perceive, grabbing all the opportunities and always having a mindset of adapting and being the change.</p>
			<p>Mr. Albert said the entire thing you do in life, at one point of time you need to give away and he encouraged the students to travel on those roads less travelled, setting greater dreams beyond measure and belief.</p>

			<p>At the end of the session, the students were given time to discuss on the topic and to clear all their doubts on the subject. Prof. Aloysius Edward, Dean Commerce and management, Head of the Department and other Faculty members were present during the ILS program.</p>



