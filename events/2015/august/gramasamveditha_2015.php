		<div class="panel-body">
		



			<p align=justify>Kristu Jayanti School of Management organized its annual REP for First Trimester MBA & PGDM students at Ethimmasandra village, ShidlaghataTaluk, Chikkaballapur District between August 13, 2015 and August 15, 2015. The main objective behind the REP is to expose the students to rural environment and contributing to the village.</p>

			<p align=justify>On the first day, Our students conducted literary events such as essay writing, spell bee and drawing competition for the government school children, the remaining students took part in Swacch Bharat cleaning up the nearby parts of the school premises. The students also carried out a detailed survey among the villagers by going to each house and collecting details to understand their state of living. On the second day, the students conducted sports and games for both primary and high school children, events such as kabbadi, musical chair, volleyball and others were conducted. The students also organized a Cultural Night for the villagers in the school premises, where different performances like dance, group song, mime and skit were displayed leaving the entire crowd amused and excited. Next day August 15th, Faculty and students celebrated the Independence Day with school children & teachers and distributed the prices to the winners of different events. Finally The 3 day Rural Exposure Programme provided a really good platform, wherethe students learnt how to manage things and to cope up with unpleasant circumstances and to utilize resources judiciously.</p>


