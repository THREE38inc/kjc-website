			<p align=justify>The department of Management Studies (MBA and PGDM) organized Expert Lecture Series on August 12, 2015. Niranjan Khatri, Founder and Principal Consultant of I-Sambhav, was the chief guest of the session. The lecture session focused on the importance of sustainable development in today's world.</p>

			<p align=justify>Mr. Khatri spoke about how our views on planet earth have gradually changed. He said, a few years back, earth was filled with infinite resources. But in recent times, we have understood that earth is finite, both as a source and a sink. He further said that the exponential growth of population is resulting in the decline of resources.</p>

			<p align=justify>While talking about new perspectives, he opined, Do things differently to capture attention. He concluded by suggesting few measures to be taken to achieve sustainable development, which include using favorable substitutes instead of harmful materials and be nature sensitive.</p>




