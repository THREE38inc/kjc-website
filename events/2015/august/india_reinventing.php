		<div class="panel-body">
		



			<p align=justify>Make in India is an initiative program of the government of India launched by Prime Minister Narendra Modi on 25th September 2014 to encourage companies to manufacture their products in India. The Prime Minister took a lion's step to make India into a global manufacturing hub and increase the employment rate as well as the growth in the country's GDP by launching the Make in India campaign.</p>

			<p align=justify>The department of management studies of Kristu Jayanti School of Management had organized a Panel Discussion on the subject matter, emphasizing on reinventing skills for the students of MBA & PGDM on 1st August which was attended by representatives from various colleges. The panel consisted of distinguished personalities from different spectrums Shri. Varadarajan Krishnamurthy( Former Chief of Bureau & Deputy Editor, The Hindu-Business Line), Shri. Raj Raj Kumar( Director & Site Leader, TE connectivity), Shri. Raj Raghavan( Director & Country HR leader, Amazon), Shri. Jason Samuel( VP- HR, TVS Motor Co Ltd) and Shri. Winston DeRosario( Director, Grant Thornton).The Panel in general reinforced the fact that Indian economy possessed greater potential to become world leaders than any other economy in the world. The panel focused on the key areas of Make in India Campaign - Defence, Infrastructure, Energy and Consumer service. The campaign does not restrain itself only towards manufacturing sector but also highlights on building a strong E-Commerce in India to transform the way India buys and sells. The growth of Indian Economy has been commendable since Independence and with the youth of the country on their toes with a greater mission of Make in India, India is sure to reach its 3rd trillion in less than 5 years.</p>

			<p align=justify>Finally the students were told on individual contribution by removing people prejudice, seeking empowerment and not employability and by embracing change. The panel in one accord urged the students who are the future of India not just to Make in India but to work for a greater cause to MAKE INDIA.</p>




