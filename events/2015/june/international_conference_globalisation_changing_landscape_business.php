			<p align=justify>Krisym, Kristu Jayanti International symposium conducts every year a series of International conferences on various themes by the Kristu Jayanti School of Management.This year Kristu Jayanti International Symposium with the advice of the Conference Patron initiated its 8th International Conference on Globalization and Changing Landscape of Business.</p>

			<p>This conference is an interdisciplinary forum for the presentation of Novel advances and research results in the field of globalization and business.The main objective of the conference is to bring together leading academicians, Industrialists, researchers and scholars to contribute their thoughts, opinions, suggestions in the area of globalization and business.</p>

			<p>This year Krisym 2015 focused on 11 themes and 12 topics. We are indeed very delighted to say that there were 12 international key note speakers from USA, UK, France, Canada, Iran, Turkey and China contributing to the value & flair of our Conference. There were around 92 delegates from across the globe who presented their papers in their domain.</p>

			<p>The Inauguration of the event was on June 30, 2015. To mark the beginning of the event we had the Presidential address by Rev Fr. Josekutty PD, Principal Kristu Jayanti College.This was followed by Inaugural address by Prof Fiona Moore, School of Management Organization Studies and Human Resource Management, Royal Holloway, University of London, UK.</p>

			<p>The other key note speakers for the 2 day conferences were Prof Justin Paul from the University of Puerto Rico, USA, Prof William Newburry College of Business. Florida International University, USA, Prof Hemant Merchant, Global Business, University of South Florida, USA, Dr. Diane A Isabelle from the Carleton Univeristy,Ontario,Canada, Dr. Cordula Barzantny,Tulouse Business School France, Dr. H Rui ,Senior Lecturer at School of Management ,Royal Holloway, University of Cambridge, London. The 3 member panelists moderated by Mr. Prammath Nath, HR Head,KPMG, presented their views and opinions on globalization and changing landscape of Business . The Expert panel members from various Countries who contributed their rich experiences were:Dr.Karen Lynden,Rowan Cabarrus Community College,USA,Dr.Omer Faruk ,Assistant Professor, Abdullah Gul University,Turkey,Dr.Diane Isabelle,Assistant Professor,Carleton University,Canada.</p>

			<p>The International conference concluded with the Valedictory Address. The Programme was addressed by Prof Ram Mudambi, Professor and Perelman Senior Research Fellow, Fox School of Business Temple University, Philadelphia, USA.</p>

			
