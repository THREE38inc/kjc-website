			<p>3i Program is one of the series conducted in Kristu Jayanti School of Management where various Managers and Leaders from IT Industry spend their time with the students sharing their experiences and values which enables the students to focus more and to adapt to every change.</p>
<p>
	The School of management of Kristu Jayanti college, had organized the Industry Institute Interface Series for the students of II Trimester MBA & PGDM on 5th December2015 to inculcate and train the students on developing the ability to perceive. The program commenced by invoking the presence of the almighty God. During the session, the students were given brief knowledge about IT Technology and Career Opportunities in Health Care. Shri. B.K Kulkarni, retired as VP Health Care Siemens Information Technology pvt ltd was invited to take over the Session. His life experiences and examples on Health Care, on Life Sciences, on IT Technology relationship did bring an entire different outlook on our perspective and did bridge the gap between IT Industry and Management. In Address to the students he shared the objectives, challenges, different roles of IT in Health Care.</p>

			<p>According to Shri. B.K Kulkarni "Health Care is all about improving the quality of Patient care and effectively monitoring hospital wide processes". Shri. B.K Kulkarni encouraged the students to take up challenges in Health Care, utilizing all the available opportunities and serving the society.</p>

			<p>At the end of the session, the students were given time to discuss on the topic and to clear all their doubts on the subject. Dr.Arul SenthilKumar Facilitated by giving away a memento of appreciation to Shri B.K Kulkarni for his efforts for taking time to speak to the students of Kristu Jayanti School of Management. Prof. Sen B Mathews, Director CPCR, along with other faculty members were also present during the 3i session.</p>



