			<p align=justify>NGO Report The NGO visit for the MBA and PGDM batch was on 17th April 2015. The students were divided into two teams and each team was sent to two different places, one batch went to infant Jesus home and the other batch to Home of Hope. Home of Hope is an organisation that is run by a person called Raja popularly known as Auto Raja. He takes those people who are on the streets and looks after and provides the basic things for them. On an average he takes almost 5 persons per day but they also do have a death rate of 5-10 people per week. At present the organisation consists of 168 people. It was a great privilege for us students to be a part of that organisation for a day. We had interaction with all those inmates who were present there and few of them sang few devotional songs and we also did few programs like dance and action songs and finally we also did pray for them. We also visited the children's home that was associated with home of hope. Here we met children who are not so fortunate enough and they are looked after by this organisation. And we distributed chocolates for them. From this visit we did learn a lot of things i.e. how one person can bring a change in another person's life and how fortunate we are to have such a blessed life.</p>



