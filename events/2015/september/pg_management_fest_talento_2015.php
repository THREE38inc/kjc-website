			<p align=justify>The Department of Management Studies (MBA & PGDM) organized a two day Intra Collegiate Management fest "Talento 2015" in Kristu Jayanti College on September 10 & 11, 2015.</p>

			<p>Shri.Sundar Varadaraj Perangur, Vice President, Business Development Strategies, Infuid labs inaugurated the two day fest. He addressed the gathering on various topics ranging from Hierarchy of Needs in modern business to effective management skills expected from managers.</p>

			<p align=justify>"Talento is a unique platform for students of management to showcase their skills in managing the day to day situations in life said Rev.Fr. Josekutty P.D , Principal, Kristu Jayanti College while delivering his presidential address. He further added on the role of final year students of management in empowering their juniors with various tasks testing their managerial skills and team work.</p>

			<p align=justify>The ninth edition of Talento had inculcated the theme Greek Warriors, with the tagline Vini Vidi Vici, meaning I Came, I Saw, I Conquered. Talento 2015 had nine events with 10 teams comprising over hundred participants.</p>

			<p align=justify>Prof. Aloysius Edward, Dean ,Faculty of Commerce and Management, Prof. Vijaya Bhaskaran, Head, MBA Department; Prof. Rakesh T S, , Faculty Coordinator; Ms. Sruthi George , Mr.Jefin Johnson and Mr.Joel Jacob, Student Coordinators were also present for the inaugural ceremony of Talento 2015.</p>




