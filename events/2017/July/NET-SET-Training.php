		<div class="panel-body">
		

			<p>Most of the candidates join a P.G. course with an intention to start a career as a Professor.  It has become an attractive option due to high salary, and social respect. For recruitment as professors in Universities/colleges/institutions a minimum eligibility condition of NET(National Eligibility Test)/SET(State Eligibility Test) Exam Pass  has been made compulsory. Generally NET Test will be conducted by the UGC twice in a year i.e. in the month of June & December every year. With that intention the Centre for NET / SET Training of the college is established to help the PG Students to pass UGC NET / SET Examinations, to enhance the quantitative aptitude of the Participants and to enhance the teaching and research aptitude of the Post Graduate Students.</p>
			<p><b>Objectives of the Training</b></p>
			<ul><li>To help the participants to pass UGC NET / KSET Examinations</li>
				<li>To enhance the quantitative aptitude of the Participants</li>
				<li>To enhance the teaching and research aptitude of the participants</li></ul>
				<p><b>Components of Training</b></p>
				<ul><li>Research Aptitude</li>
					<li>Teaching Aptitude</li>
					<li>Reading comprehension</li>
					<li>Communication</li>
					<li>Reasoning</li>
					<li>Logical Reasoning</li>
					<li>Data Interpretation</li>
					<li>Information and Communication Technology</li>
					<li>People and Environment</li>
					<li>Higher Education System</li></ul>

