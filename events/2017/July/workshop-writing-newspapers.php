            <p align=justify>The Department of Journalism and Mass Communication organised a workshop on ‘Writing for Newspapers - Professional Perspectives' , on July 26, 2017 for MA Journalism and Mass Communication students. Shri. B Pradeep Nair, News Editor, ‘The Hindu’ talked about the writing skills required for a newspaper, challenges faced by reporters, structure of writing news and  different types and steps required for newspaper articles.  He also emphasized on the importance and interest to be brought into news by the reporters. And that the reporters have to be ethical regardless of the situations they face. It was an interactive session, as the speaker touched in detail on the reality aspects of journalists in the present times.</p>

