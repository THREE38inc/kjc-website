			<p><b>Objective:</b>  To impart knowledge on different types of satellites, purpose of satellites, life term of satellites, life inside the satellites, cost of the satellite and techniques to reduce the space jam.</p>


			<p align=justify>
				The guest lecture on SPACE TECHNOLOGY organized for I and III sem BCA, B.Sc. students on 01.07.17 in Main Auditorium at 11.00 Am. The speaker was Srinivas HL, Scientist from ISRO satellite center, Bangalore. He started his lecture with launched satellites such as Aryabhatta, Chandrayan, IRM and its purposes. Speaker played video on    how scientists spending their life and doing their daily routines inside the satellite.  The components used for covering satellites are explained with videos. He uncovered the reasons for launching Indian satellite from Sriharikota and for using American components for constructing Indian satellites. The details such as the cost of making Indian satellites are 400 crore, American satellites are 1800 core, Life term and Quality of American satellite’s components are provided to the students. The session ends with questionnaire. Speaker clarified students’ queries like role of computer graduate students in space, how to get in to space technology related courses, how obsolete satellites are destroyed, how space jam is avoided, about international space committee and International space stations. </p>

			
