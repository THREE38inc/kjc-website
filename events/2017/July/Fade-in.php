            <p align=justify>Kristu Jayanti College- Film and Photography Club is organizing a two-day film festival ‘Fade in:’ on July 27 and 28. Mr. Georgekutty AL, renowned film scholar and Secretary, Bangalore Film Society; will be inaugurating the fest.</p>


            <p align=justify>This year being the 25th death anniversary of legendary film maker Satyajit Ray, ‘Fade in:’ pays a tribute by screening the notable works of Ray ‘Pather Panchali’, and ‘Charulatha’.</p>


            <p align=justify>‘Fade in:’ will also screen masterpieces like Charlie Chaplin’s ‘The Great Dictator’, Tom Tykwer’s ‘Run Lola Run’, Asghar Farhadi’s ‘Salesman’ and M. Manikandan’s ‘Kakka Muttai’.</p>

