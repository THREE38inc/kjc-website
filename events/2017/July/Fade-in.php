 ‘Fade in', Film Festival

            <b class="panel-title pull-right">July 27<sup>th</sup> - 28<sup>th</sup> 2017</b>
            <div style="clear:both"></div>
        </div>
		<div class="panel-body">
		

            <p align=justify>Film and Photography Club of Kristu Jaynti College is organizing a two-day film festival ‘Fade in:’ on July 27 and 28.   Shri. Georgekutty AL, Founder Editor of Deep Focus and Secretary of Bangalore Film Society inaugurated the event. ‘Fade in' is an annual event conducted by the Journalism & Mass communication and English Departments of our college to cultivate serious film viewing among our students and also to expose them to artistic films.This year being the 25th death anniversary of legendary film maker Satyajit Ray, ‘Fade in:’ pays a tribute by screening the notable works of Ray, including  ‘Pather Panchali’, and ‘Charulatha’. ‘Fade in:’ also  plans to screen masterpieces like Charlie Chaplin’s ‘The Great Dictator’, Tom Tykwer’s ‘Run Lola Run’, Asghar Farhadi’s ‘Salesman’ and M. Manikandan’s ‘Kakka Muttai’. Around 200 students participated in the programme. </p>

