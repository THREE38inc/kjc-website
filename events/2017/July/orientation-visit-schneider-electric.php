  Department of Social Work | Orientation Visit to Schneider Electric

            <b class="panel-title pull-right">July 01<sup>st</sup> 2017</b>
            <div style="clear:both"></div>
        </div>
		<div class="panel-body">
		

            <p align=justify>The Department of Social Work has organized an Orientation Visit for the 3rd Semester of MSW students to Schneider Electric on the 1st of July 1, 2017. Schneider Electric is a French multinational corporation that specializes in energy management and automation solutions, spanning hardware, software and services. The students were accompanied by <b>Dr. I. Jerry Richards</b>, faculty member of the Department of Social Work. On arriving at the industry, the students were divided into two groups and each group was escorted by a guide. The students were taken around the industry in their respective groups by their respective guides. They were explained about the roles, responsibilities and functions of the Human Resources Department in the Industry. Furthermore, the students were exposed to various production of wire and digital energy meter box. The students had a great exposure of the working environment of an industrial setting and the observation visit was worthwhile. </p>



			
