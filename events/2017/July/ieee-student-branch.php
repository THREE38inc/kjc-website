            <p align=justify>The first session of 3i- Industry Institute Interface for this academic year was hosted on “Impressions.”  The session that was vibrant, contagious and exuberant initiated the students into both college and academic disciplines. Mr. Aditya Jain, a renowned IEEE member, was the resource person of the day. The session commenced with a demonstration of the art of public speaking. It led the audience into realistic issues that they could potentially face in their upcoming professional lives. Nullifying the myths and generalized notions of most people, the speaker reassured the stage-fear in an individual as a sign of perfectionism.  Furthermore, He said, No person is born with the fortune to succeed. Instead, they are born with abilities. The success or failure in implementing something is purely based on how consciously or unconsciously they work on it. The interactive dialog electrified the atmosphere and raised much response. He touched some aspects of confident public speakers who made blunders and came out with utmost confidence and the same spirit of perseverance</p>


			
