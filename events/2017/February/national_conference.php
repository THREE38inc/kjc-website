<p align=justify>P G Department of Mass Communication & Journalism and English are organizing a <b>'National Conference on Narratives in Media and Literature'</b> on February 07 2017. This conference provides the academic space to explore varied narratives that are produced and deployed in film and literature.  Narratives reflect ideologies and are therefore practices of being and becoming.  The mechanics of a narrative brings into focus the politics of representation.  The conference directorate invites papers related to the broad theme “Narratives in Literature and Media”.  The students and faculty form the disciplines of Literature, Media Studies, Journalism and Culture Studies to present unpublished, original papers at the conference.  </p>

