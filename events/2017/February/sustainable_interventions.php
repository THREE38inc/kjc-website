      Department of Social Work | Expert Lecture on Sustainable Interventions in Community Development


            <b class="panel-title pull-right">February 27<sup>th</sup>  2017</b>
            <div style="clear:both"></div>
        </div>
		<div class="panel-body">
		



			<p align=justify>The Department of Social Work organized an expert lecture on "Sustainable Interventions in Community Development" by Mr. David Kumar A (Senior Manager, Community Initiatives, United Way, Bangalore) for the MSW students.</p>

			<p align=justify>The Resource person was felicitated by Dr. Jonas Richard A., (Head, Department of Social Work, Kristu Jayanti College) during the welcome address.</p>

			<p align=justify>Mr. Kumar began the session by enumerating his experiences as a student of Social Work wherein he volunteered with many Non-Governmental Agencies in Community Development. The speaker shared that the experiences gathered has empowered him to specialize in Community Development. Consequently, he has worked more than 150 projects including several Government Agencies, Multinational Companies and with Communities at the grass root level. After which, Mr. Kumar drew out the differences between the terms Project, Campaign and Programme.</p>

			<p align=justify>The resource person then shared the details of the "Wake the Lake" and "Born Learning" campaigns. 'Wake the Lake' Campaign aimed at designing ways to revive the lakes in and around Bangalore whereas "Born Learning" campaign focused on working extensively with the State Government to ensure that governmental benefits reach the malnourished children under 6 years of age. Mr. Kumar educated the students on strategies to engage the local people in empowering communities. The session grew intriguing, when the speaker shared the various cost effective interventions that are implemented for training the children in the Anganvadis. These interventions among a few include the use of colored seeds and marbles to increase the fine and gross motor skills of the children. Further, Mr. Kumar elaborated on the process of implementation of Community Development projects.</p>

			<p align=justify>Mr. Kumar enlightened the students about the various programs like "Citizens for the City,,"Neighbourhood Improvement Program","Namma Nadi","Halli Habba","Under the Sky" and "Dine with the Celebrities" that aid in fund raising for the marginalized communities.</p>

			<p align=justify>Mr. Kumar also encouraged the students to consider Community Development as an innovative area of Social Work, where the students can coordinate at the grass root levels and strengthen the society and nation at large.</p>

			<p align=justify>The expert lecture concluded with an interactive session where the students raised their doubts about various issues regarding sustainable development like the support and aid from government for these projects, use of eco- friendly models of deities for festive purposes, sustainability and duration of projects.</p>
			
