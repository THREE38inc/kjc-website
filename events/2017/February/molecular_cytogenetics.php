       Guest Lecture On The Role Of Molecular Cytogenetics In The Diagnosis Of Cancer 
            <b class="panel-title pull-right">February 25<sup>th</sup>  2017</b>
            <div style="clear:both"></div>
        </div>
		<div class="panel-body">
		



			<p align=justify>On the 25th of February 2017 a guest lecture was conducted by the Department of Life Science for both the PG and UG students to impart knowledge on various DNA probes in the molecular diagnosis of cancer. Dr. B. Lakshman Kumar , Application Specialist, Probes Metasystems India (Bangalore) P Ltd., Bangalore, was the guest speaker and he used his immense experience to stimulate student's minds. Lakshman Kumar provided a summary about the karyotyping process and detailed the various types of fluorescent dyes that are used to stain DNA. In light of this, he explained the applications of FISH (Fluorescent In-Situ Hybridization).</p>

			<p align=justify>He also mentioned that, chromosomes can now be stained in different colours and these dyes are called chromosome paints. The lecturer spoke about a new set of dyes, Xcyte, that his company released that could stain a single chromosome in multiple colours. Each color denotes a particular region of the chromosome. Performing this kind of staining is very useful during the identification of chromosome translocations and could potentially be used to assess the danger of pre-malignant lesions.</p>

			<p align=justify>Students were also enlightened on the benefits of knowing how to perform a karyotype and it's financial prospects. FISH is cheaper to use than NGS (Next Generation Sequencing) and is very sensitive. It has a good TAT (Turn Around Time) and hence results can be obtained reasonably quick and this is essential in diagnostic laboratories.</p>

			<p align=justify>Students found this talk very useful and they had interacted well with the guest speaker. Guest speaker also gave information on employment opportunities in the academic, industry and research & development sector which students found helpful.</p>


