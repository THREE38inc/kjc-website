			<p align=justify>World radio day was observing by the Second Semester M A Mass Communication & Journalism students at Kristu Jayanti College P G quadrangle on 15th February 2017 with 7.55 minutes Radio Production during the second break at 10:50 A.M.</p>

			<p align=justify>This radio production portrayed the importance of old and new radio, RJ’s chat, Fr. Josekutty P D, Principal’s memory about radio. It consisted of variety like tongue twister by students and faculty dialogues from other departments as well.</p>

			<p align=justify>Few art installations such as a globe with headphone, an old radio set were set up in quadrangle. The program was a collective effort of the students with an Art team consisted of Angel, Nikhil, Sheetal, Ashyl, Bhumika, Samantha, Alda and Jissy. The RJ's were Anurag, Christena, Sheetal and Anju. Script was writen by Sharlet and Ajith. Technicalities were handled by Anand and Ajith. After airing the program, Father Principal esteemed the work of MCJ students for counting the past glory and the present significance of radio.</p>

			
