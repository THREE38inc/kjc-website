      Department of Social Work | Expert Lecture on Changing Paradigms in Industrial Relations 
            <b class="panel-title pull-right">February 25<sup>th</sup>  2017</b>
            <div style="clear:both"></div>
        </div>
		<div class="panel-body">
		



			<p align=justify>The Department of Social Work organized an expert lecture on “Changing Paradigms in Industrial Relations” on 25 February 2017.</p>

			<p align=justify>The esteemed resource person for the expert lecture was Mr. Rajasekaran (Manager- HR, Indian Telephone Industries Ltd) was felicitated by Dr. Jonas Richard A. (Head, Department of Social Work, Kristu, Jayanti College).</p>

			<p align=justify>Mr. Rajasekaran initiated the session by highlighting the fact that India being a Labor intensive economy, must incorporate changes both at personal and organizational levels.</p>

			<p align=justify>The expert speaker introduced the students to the current scenario at the private and public sector units which were accompanied by indicative statistical data. He further shed light on the relevance of Human Resource Development in the age of urbanization and accelerated technological changes insisting that customer values and customer decisions form the foundation of business.</p>

			<p align=justify>The students were educated on the rewarding practices in HR such as clarity in communication, avoiding bureaucracy, building trust, empowering employees and personal integrity. The impediments in the profession of HR such as stress and politics were also discussed.</p>

			<p align=justify>Towards the end of the session Mr. Rajasekaran enlightened the students on the latest trends and applications towards which the HR professionals should be wary of such as cloud computing, cognitive computing, artificial intelligence and singularity.</p>

			<p align=justify>The lecture concluded with an interactive session wherein the students honed their analytical abilities and were encouraged to foresee the need for improvement and take counter measures to get productive yields.</p>

			<p align=justify>The students expressed their gratitude to Mr. Rajasekaran for his valuable insight and efforts. After which, Dr. I. Jerry Richards (Faculty, Department of Social Work, Kristu Jayanti College) presented the speaker with a memento as a token of appreciation</p>
			
