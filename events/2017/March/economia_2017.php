			<p align=justify>Economia is a One Day Intra Collegiate Economics fest conducted by the Department of Economics, Kristu Jayanti College. It is planned & organized by the Department of Economics under the direct supervision and guidance of the staff co-coordinators. It proactively looks into the impact of current and emerging economic policies in ways that advance understanding of the same and critically analyzing their effectiveness.</p>

			<p align=justify>A theme oriented fest; the theme for Economia 2017 was "The recent economic initiatives for India's growth". It was held on the 2nd of March 2017 in the Mini Audi of the Kristu Jayanti campus. The key events in the fest were Quiz, Debate, Essay writing and Model making / Poster competitions.</p>

			<p align=justify>The Chief Guest for the Inauguration was Mr Anadi Charan Sahu, General Manager & Regional Head, SIDBI. Father Lijo P Thomas, Financial Administrator of Kristu Jayanti College, gave the Presidential address wherein he stressed the importance of the subject in the present world. Mr A C Sahu spoke on the changes that is expected to be seen in the near future with the recent economic initiatives that the Modi Government had undertaken. He cited initiatives of SIDBI in similar lines.</p>

			<p align=justify>The Guest of Honour for the Valedictory session was Dr Malini Lakshminarayan Tantri from ISEC. She gave a brief lecture on the economic reforms in India - past and present - the causes as well as effects of such changes. She motivated the students into taking research in Economics seriously. Father Augustine George, Vice Principal of the college, expressed his happiness at the successful conduct of the event. Finally, prizes for the various events that was conducted was distributed by Father Augustine George, Dr Malini L Tanri and Dr Aloysius Edward, the Dean of Faculty of Commerce & Management.</p>

			<p align=justify>The fest did provide a platform for exhibiting the skills and talents of the numerous participants as well as learning from peers. </p>
			
