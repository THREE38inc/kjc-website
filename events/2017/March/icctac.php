			<p><b>About Conference</b><br>
IEEE ICCTAC-Keeping Pace with Technology<br>
				The unified efforts towards the faculty development and growth, encourages them to embrace constant changes in technology. Owing to the rapid changes in the field of Computer Technology, it is necessary to conduct conferences to bridge the gap between the current technology and new emerging technology. In this regard the department has successfully organized three editions of National Conference (Current Trends in Advanced Computing) and four editions of International Conference which were sponsored by ISRO, Bangalore. The conference proceedings were published in international journal of computer application (IJCA). Last year the department has conducted international conference in association with IEEE Bangalore section and international conference on current trends and advanced computing was technically co-sponsored by IEEE Bangalore chapter and proceedings has been included in IEEE Xplore. In continuation of accomplishing international exposure the department is planning to organize the fifth edition of International Conference in the month of March 2 & 3, 2017. The Conference would provide an international forum for the presentation and discussion of the emerging technologies in the field of Computer Science and Information Technology.</p>

			<p><b>Call for Papers</b><br>
The IEEE International Conference on Current Trends in Advanced Computing (ICCTAC) will provide an excellent international forum for sharing knowledge and results in theory, methodology and applications of recent trends in Information Technology. The aim of the conference is to provide a platform to the researchers and practitioners from both academia as well as industry to meet and share cutting-edge development in the field of Computer Science. Authors are solicited to contribute to the conference by submitting articles that illustrate research results, projects, surveying works and industrial experiences that describe significant advances in the following areas, but are not limited to.<br>
• Mobile Computing<br>
• Parallel Computing<br>
• Cloud Computing<br>
• Data Mining<br>
• Text Mining<br>
• Web Mining<br>
• Image Processing<br>
• Artificial Intelligence<br>
• Big data Analytics<br>
• Internet of Things<br>
• Embedded Systems<br>
• Bio-inspired computing<br>
• Networks<br>
• Data Security<br>
				• Software Engineering</p>

