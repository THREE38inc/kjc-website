			<p><b>Paper Submission Guidelines</b><br>
• Simultaneous submissions (papers already submitted to other conferences/ journals) are not allowed.<br>
• All papers must follow strict IEEE formatting instructions. Violation of which leads to rejection of the paper.<br>
				• Please do not submit plagiarized papers. Strict actions will be taken against the authors of those papers.</p>

			<p><b>Paper must be in pdf format and must satisfy following requirements:</b><br>
• Paper Size: US Letter format (8.5" x 11").<br>
• Paper Length: Up to 6 pages, including figures, tables & references. A maximum of two additional pages are permitted, with an additional charge of INR 1000 per page, to be paid during author registration.<br>
• Paper Formatting: double column, single spaced, 10pt Times New Roman font.<br>
• Margins: Left, Right, and Bottom: 0.75" (19mm). The top margin must be 0.75" (19 mm), except for the title page where it must be 1" (25 mm).<br>
• File Size Limitation: 5.0MB.<br>
				• No page numbering on the manuscript is allowed.</p>

			<p><b>Note:</b> Violations of any of the above specifications may result in rejection of your paper.</p>

			<p><b>Review Process:</b><br>
• Blind review process of peers will be as follows:<br>
• Minimum of 3 reviewers per paper; all reviewers are subject matter experts as defined by their edict (Reviewer usually have 5 or 6 edicts that they are willing to review against)<br>
• Each reviewer will be given maximum 5 no. of papers to review in minimum of one month time<br>
• The number of submissions will determine the number of reviewers required<br>
				• Review Committee initially checks the plagiarism in the submitted paper by plagiarism checking tools.</p>

