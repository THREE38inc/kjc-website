			<p align=justify>The Department of Social Work, Organized a series of Special Lectures on 15th â€“ 22nd March, 2017 on the topic "Sevaaya Samarpanam" for the outgoing UG Jayantians at the Mini Auditorium I and II, Kristu Jayanti College. The sessions emphasized to impart a sense of Social Responsibility and empower the minds of all outgoing Jayantians to rededicate themselves to the welfare of the society. The orientation programs were conducted in different sessions for the B.Com, B.B.A, BSc CSME, BCA, BA HEP, HTJ and JPSEng.</p>

			<p align=justify>Dr. Jonas Richard A. (Head, Department of Social Work) welcomed the staff, students and moderated the session to enlighten the mind of students by sharing his experience and throwing light on the objectives of Sevaaya Samarpanam.</p>

<p align=justify>The objective of the Sevaaya Samarpanam are as follows<br>
1. To trigger the social consciousness in the minds of every Jayantian in the light of the motto of the college i.e. Light and Prosperity.<br>
2. An effort to impart a sense of social responsibility in the mind of all outgoing Jayantians and empower them to dedicated themselves to the welfare of the society.<br>
	3. To strengthen Jayantians to contribute maximum to society with meaningful and professional manner.</p>

			<p align=justify>During the session Dr. I Jerry Richards (Faculty member, Department of Social Work) presented and explained the need for Social Work, the Methods of Social Work, Scope of Social Work in Indian and Western Countries. Dr. Winnie Joyce A. (Faculty member, Department of Social Work) explained about the different programs and academic curriculum provided by the Department of Social Work, Kristu Jayanti College. Dr. Emmanuel Daniel (Faculty member, Department of Social Work) shared his views on the emerging areas of job opportunities in the field of Social Work</p>

			<p align=justify>During the question and answer session, the students posed questions on opportunities in the field of Social Work, Social Work camp, field work and block placements. Dr. Jonas Richard A. clarified the questions asked by the students and also encouraged the students to consider Social Work as one of the bright career options in the current scenario. </p>
			
