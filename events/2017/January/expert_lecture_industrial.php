			<p align=justify>The Department of Social Work of Kristu Jayanti College organised an Expert Lecture on the Industrial Social Work on 19th of January 2017. The Expert lecture aimed at enlightening the students of II MSW on the scope and skills required by professional Social Workers in the Industry settings.</p>

			<p align=justify>The Expert lecture began with a welcome address given by Dr. Jonas Richard A. (Head, Department of Social Work) and introduced the guest speaker Mr. Israel Inbaraj (General Manager, HR Department, Tata Consultancy Services) for the session.</p>

			<p align=justify>The session started with the students in splitting into 5 groups as directed by the speaker. Each group was encouraged to formulate relevant questions pertaining to the field of HR.</p>

			<p align=justify>The speaker highlighted that the focus of MSW HR is more towards human psychology, industrial psychology, resource development, empowerment, engagement and support.</p>

			<p align=justify>Then, the speaker shed light on the various CSR activities undertaken by TCS, their main core of CSR revolving around providing education to the marginalised population. Mr. Inbaraj shared the various gender related issues that existed in the corporate systems and the various company policies in promoting gender equality. The speaker highlighted the significance of knowledge on basic financial concepts in order to prepare project proposals and budget approval related matters in the Industrial sector.</p>

			<p align=justify>The speaker concluded the session by stressing the identification of core skills which needs to be developed according to the industrial requirement. The students expressed their gratitude to the speaker for an informative and thought provoking meeting.</p>

