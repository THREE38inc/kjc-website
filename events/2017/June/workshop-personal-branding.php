			<p>The Department of Social Work organized a workshop on “Personal Branding and Employability Skills” for the III Semester MSW students on the 30th of June, 2017. The Resource person was <b>Prof. Sen B Mathews,</b> Director, Centre for Placement and Corporate Relations, Kristu Jayanti College. </p>
			
