			<p><b>Focus on making the Arts and Science students Industry Ready stands crucial for the holistic development of students passing out of college.</b></p>
<p>As a first step to it, Kristu Jayanti College, Bengaluru signs membership with ICT Academy<br>
	Of late with the growing importance for the Arts and Science colleges across India, majority of the students are enrolled in the Arts and Science Colleges. It has also been stated that the maximum number of Ph.D has been enrolled from the Science streams.</p>
			
<p>As per the All India Survey on Higher education report by Ministry of Human Resource Development, GOI, it is stated that 
	<b>“At Undergraduate level the highest number (40%) of students is enrolled in Arts/Humanities/Social Sciences courses followed by Science (16%), Engineering and Technology (15.6%) and Commerce (14.1%). At Ph.D. level, maximum number of students is enrolled in Science stream followed by Engineering and Technology.”</b>
	Realizing this various arts and science colleges gives a special focus on training the students in various industry relevant skill development course. Kristu Jayanti College, Bengaluru has signed the Membership with ICT Academy on June 23, 2017. The membership is signed to benefit Faculty Members and Students through various initiatives of ICT Academy which includes Faculty Development, Student Skill Development, Industry-Institute Collaboration, Research etc.</p>
			
<p>Kristu Jayanti College, Bengaluru is the first Arts & Science College in the State of Karnataka to sign the membership with ICT Academy.</p>
	
			<p>The Certificate of Membership was exchanged between <b>Fr. Jose Kutty P.D the Principal of the institution and Mr. Suresh Babu, Regional Head – Karnataka & Telangana, ICT Academy.</b> Speaking on the occasion, Mr. Suresh Babu said, “ICT Academy the non-profit organization is happy to sign its first arts & science college membership in the state of Karnataka. We are committed to provide full support to the faculty and students of the college by providing world class training and delivering courses directly from the global corporate house. We strive to bring down the skill gap by being a bridge between academia and corporate, and this occasion is the first step towards that.” Mr. Vishnu Prasad, State Head – Karnataka, ICT Academy was also present on the occasion.</p>
			
