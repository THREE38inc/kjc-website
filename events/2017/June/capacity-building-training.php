            <p align=justify>The Centre for Life Skills Education (CLSE), Kristu Jayanti College, in collaboration with the Rajiv Gandhi National Institute of Youth Development (RGNIYD), an Institute of National Importance under Ministry of Youth Affairs & Sports, Government of India, organized the Life Skills Education for the first semester students from June 13-17, 2017. The five day programme was inaugurated by <b>Rev. Fr. Josekutty</b> P D, Principal and <b>Fr. Augustine George</b>, Vice Principal.  The sessions were effectively handled by different resource persons on ten of the major skills - Self Awareness,  Empathy, Critical Thinking, Decision Making, Problem Solving, Creative Thinking, Coping with Stress,  Effective Communication,  Interpersonal Skills and Coping with Emotions. The sessions were creative, innovative and got the students involved in various activities which enabled them to identify their potential and sharpen the skills. As the students begin their new journey, they are prepared through the Life Skills Education to face the competent world which seeks excellence. Each student was given the opportunity to improve and inculcate the true values of Life within. Students remarked that the programme was quite meaningful and constructive in building the level of confidence and effectiveness within every individual.</p>
			
