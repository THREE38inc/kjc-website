            <p align=justify><b>Date: 30.06.2017</b><br>
                With the objective to explore and appraise careers and prospects in Psychology for UG & PG Students. Dr. Sudha Bhogle, Head, Student Solutions, Eduquity Career Technologies, addressing around 220 psychology students from under graduation and post-graduation started her seminar on Emerging areas and Avenues in Psychology by calling Psychology to be “the go to Science”. As the seminar progressed she spoke about the various branches in psychology and its major function areas, scope and the career prospects in those branches. The following are the major branches which were covered, Cognitive psychology, Neuropsychology, Clinical psychology, Media and Technology psychology, Industrial and Organizational psychology and Engineering psychology. </p>
            <p align=justify>She gave students an idea about what they could do next to pursue a career in psychology. She ended her speech with a quote saying that “the future of psychology is so bright that we will have to wear dark shades”. </p>


