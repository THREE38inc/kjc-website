<p align=justify>This Webinar provided an exposure to the most useful feature of Microsoft Project 2010, which is one the most used software for project management. The main topics covered in the Webinar are Data Entry & Basic formatting, Working with Formulas, Working with Lookup, If & Switch functions, Changing work time, Work breakdown structure, Recurring task, Group by & Sort function, Resource assignment & leveling, Project Reports, Combining Multiple projects & Resource pooling and Project monitoring using Earn value concept.</p>
    <p align=justify>Mr. Yash explained Project management is the planning and organization of an organization's resources in order to move a specific task, event or duty toward completion. Project management typically involves a one-time project rather than an on-going activity, and resources managed include both human and financial capital. He also added that it is often closely associated with engineering projects, which typically have a complex set of components that have to be completed and assembled in a set fashion in order to create a functioning product. He described how Project managers use visual representations of workflow, such as Gantt charts and PERT charts, to determine which tasks are to be completed by which departments. Mr. Yash clearly demonstrated how project management tasks can be easily performed with MS Project Software. </p>


			
