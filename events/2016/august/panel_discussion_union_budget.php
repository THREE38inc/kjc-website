			<p>The program started at 2.00 pm in SKE auditorium. Dr. B.L Bhagyalakshmi, Former Director, Dept. of college of education, Govt of Karnataka has acted as Moderator of the Panel discussion on UNION BUDGET 2016-17. She has stated some of significant budgets from the Independence of our country. She has pointed out some of economic reformations and developments from the earlier budgets. Then she invited the first panelist to discuss the budget and taxation.</p>

			<p>Mr. Parameshwaran E.R, Associate in BMR has displayed some of highlights in the union budget 2016-17 through powerpoint presentation. He stated tax benefits to government and startup business. He also pointed out service tax increases (0.5%) on pubic service access and income tax increases on creamy layers and corporate. He stated the luxurious taxes and some direct & indirect taxes towards the public.</p>

			<p>Then moderator invited the second panelist Prof. Devandra Babu from Institute of social & economy change (ISEC). He started his discussion on Budget & Economy. He pointed out some economics theories on budget preparation and how the current budget contributes the economic growth. He listed the GDP, percapita income, inflation rate and our RBIsâ€™ contribution to maintain the currency value.</p>

