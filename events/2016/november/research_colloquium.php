			<p align=justify>Research colloquium is a programme organized by the department of Computer which is conducted once in every semester since 2011. This is the eleventh session of research colloquium. This session which is on- How to prepare a research proposal and which are the funding agencies and how to identify the quality of journals that we choose for publications</p>

			<p align=justify>The first part of the session dealt with the W5H principles- Why, Who, What, Where, When and How to proceed with Projects. She presented the model of writing a project proposal including what all aspects to take care while preparing Budget</p>

			<p align=justify>Second part of the session was information on the various funding agencies, and their probable-“Call for Proposal’ time.</p>

			<p align=justify>The third part gave us insights on how to identify a journal is indexed by approved agencies or not. She mentioned the currentl7y accepted indices as – Scopus (Reuters), SCI (Elsevier) and Global Impact factors elaborately.</p>

			<p align=justify>The session was very informative and inspiring. She also offered to assistance and support to faculty members in preparing of proposals and writing of quality research articles.</p>



