									<p><b style="color: #cd6e08;font-weight: bold;">Dr. G. Madhavan Nair</b><br>
										“Space technology is a luxury outwardly, but a necessity for any developing nation.” says Former ISRO Chairman at Vichaarmanthan, Kristu Jayanti College.</p>
									
									<p align=justify>“It’s a general belief that space technology is a luxurious affair. But according to a study carried out by the Madras School of Economics, the advances and facilities gained as a result of satellite technology are 1.5 times more valuable than what is invested for space technology”, said Dr.G. Madhavan Nair, Chairman, High Power Committee, National Civil Aircraft Development, Govt. of India & Former Chairman, ISRO.  Dr. Madhavan Nair was interacting with the students of Kristu Jayanti College on the occasion of ‘Vichaarmanthan- an interaction with Indian and global visionaries.’</p>

									<p align=justify>He said that space technology is an exciting and challenging field but though a luxury outwardly, it is a necessity in a developing country like India. He also enlightened the students on the innovative ideas of tele-education and tele-medication through space technology in this advanced world. “Through tele-education the problem of shortage of good teachers are very effectively dealt with. The facility of tele-medication through Indian space technology is even reaching out to the under-developed nations of Africa and South America”, said the pioneer of PSLV mission. In his address, Dr. Madhavan Nair also spoke on the application of satellite technology in India namely the DTH system, weather forecasting system and earth observation system. He also added that through Space technology India saw almost cent percent increase of yield in both fishing and agricultural sector.</p>

									<p align=justify>While interacting with the young minds of the college, Mr. Nair emphasized on the need to break away from the conventional roots and to focus on improving the quality of life of the people around. He stressed on the importance of the betterment of productivity, healthcare, energy sector and water security of the nation. “The technical skills of the youth should be married with the demands of the country”, he said.</p>

									<p align=justify>The students enthusiastically took part in the interactive session with the great visionary. They quizzed Dr. Nair on various issues of applications of space technology ranging from terrorism to space elevators to the setting up of the International Space Station. Rev. Fr. Sebastian T.A., Principal, Rev. Fr. Jose P.J., Financial Administrator and Head, Department of Social Work, Rev. Fr. Auustine George, Head, Department of Computer Science, Prof. Aloysius Edward J., Staff coordinator, Vichaarmanthan and Ms. Rini Abraham, Student coordinator, were also present during the occasion.</p>
									
									
