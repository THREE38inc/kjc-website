									<p align=justify>Vichaarmanthan - a platform that gives the students of Kristu Jayanti College an opportunity to interact with Global and Indian Visionaries was conducted on September 26, 2012, in Kristu Jayanti College. A dynamic personality, Dr. Pawan Agrawal, CEO, Mumbai Dabbawala Association, was the speaker of the12th edition of Vichaarmanthan. Rev. Fr. Augustine George, Head, Computer Science Department, Prof. Aloysius Edward, Programme Coordinator; Prof.Nelson Michael, Head, Management Studies and the Student Coordinators, were also present.</p>

									<p align=justify>“Our watches may go wrong, but the dabbawalas will never go wrong”, said Dr. Agrawal. He backed his statement by saying that so far there hasn’t been a single error over 120 years of service. A business that has over 5000 dabbawalas, delivering 200,000 dabbas a day and no single error in their service was very surprising. And to add to this, these dabbawalas are mostly illiterate. The reasons behind this flawless service are the simple values that they possess. Dabbawalas firmly believe that Customer is God and Work is Worship. The value of the customer is considered higher than anything. Even money is secondary to them. They haven’t gone on a strike nor has there been a single police case against them since 1890. “Money should not be your main motive in life, take ownership over your work and money will follow you instead of you running behind it”, said Dr. Agrawal.</p>

									<p align=justify>Mumbai Dabbawalas have never been late in delivering their dabbas inspite of delay in public transport and that is because they are committed to their work. They travel around 120 - 140 kilometers every day carrying around 60 -70 kilos of dabbas daily. Their aim is to reach the allotted destination 20 minutes before their committed time. Dr. Agrawal gave these examples so that the students can learn how important how important it is to commit yourself to being punctual. “If you decide to reach on time, nothing can ever stop you”, were the words of Dr. Agrawal. Even Prince Charles of England came at the allocated time to meet the dabbawalas because they couldn’t afford not delivering dabbas just to meet the Prince of England.</p>

									<p align=justify>Dabbawalas take pride in their work and follow certain disciplines. “They will not consume alcohol or smoke during work hours, they will always wear a white cap, carry an ID card and will never take a leave of absence without prior notification”, said Dr. Agrawal. He was more than happy to answer the questions the students had in store for him. All the questions were answered on a more practical approach than theoretical. He ended on an emotional note thanking each and everyone for giving him an opportunity to share his experience and encouraged the students to use every opportunity to contribute to the society.</p>
									
									
