                        <h3>Paper Submission Guidelines</h3><h4><br>
Manuscript</h4><br>

                        
                        <p>The manuscript should be submitted in Word format in British English<br>
The size of the manuscript should not exceed 4000 words<br>
Font to be used is Times New Roman, 12 size<br>
1.5 spacing has to be followed throughout the article.<br>
                            The left margin has to be 1.5”, the right, top and bottom margins have to be 1”</p>
 <br>  
                        <h4>Structure of the Paper</h4><br><p>
•	Title page: The title page should contain Specific Title, Author’s name, Affiliation and  Contact details.<br>
•	Abstract : An abstract of the research work along with key words and JEL Code.<br>
•	Introduction/literature review: An apt introduction with relevant and latest review of literature in the field would highlight the importance of the study.<br>
•	Purpose of the study/hypothesis/problem statement: A clear statement of the problem, objectives, hypothesis would showcase the need for the study.<br>
•	Methodology/procedures/research design: A succinct process of methodology of the research should be charted along with the analysis plan and data analysis software used.<br>
•	Major findings/results/analysis/discussion: This section would discuss the findings of the study in light of the literature reviewed and objectives framed.<br>
•	Summary/conclusion/ideas for future studies/implications:  The conclusions derived from the analysis with suggestions or policy implications may be presented here.<br>
•	References: A complete list of research cited in the paper has to be present in APA style.<br>
•	Notes /acknowledgements: Additional notes and acknowledgements may be presented.<br>
•	Appendices: Pertinent and related information that could not be presented in the paper may be shown in appendices.<br>
•	Tables, charts, figures, statistical data: All numerical results should be presented in the form of either tables or figures, but not both. The tables have to be numbered continuously with suitable titles and presented at the end of the paper.<br>

                 <br>       <h4>Reference Style</h4><br><P>
APA style of reporting, citation and referencing has to be followed.
<br><h4>
                     <br>   Submission Procedure<br></h4><p><br>
•	Articles may be sent as a Word File to chavara@kristujayanti.com.<br>
•	Authors are requested to submit a copyright agreement form along with the manuscript of the paper for publication
The article submitted to the Journal for publication should not be currently under review by any other journal or publishers or already been published or will be published elsewhere.<br>

