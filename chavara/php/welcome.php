                 <h3>Welcome</h3>
                 <p>We are pleased to announce the launch of CHAVARA - International Journal of Business Research. It is a peer reviewd, bi-annual research journal with an international scope published by the School of Management, Kristu Jayanti College(Autonomous), Bengaluru. It aspires to raise the platform of business research by drawing the creativity and innovation of researchers across the globe.
                </p>
                 <p>The journal is a tribute to St.Kuriakose Elias Chavara, Founder of CMI Congregation, a nineteenth century educationist and social reformer. He envisioned social emancipation through education. It is an embodiment of his vision to spread the light of knowledge to the whole world.
                 </p>
