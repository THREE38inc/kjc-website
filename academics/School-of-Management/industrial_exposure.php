								<p><b>International Industrial Visit Singapore & Malaysia</b><br>
									<b>Date:</b> july 02nd to 09th - 2014</p>

								<p align=justify>The 2nd year students of MBA/PGDM (2013-15) were given the opportunity to expose themselves to the International stage. A team of 80 members including three faculty and one tourist guide left for Singapore. Our college had given us the platform to raise our vision to a higher level.</p>

								<p align=justify>We were asked to report in the college premises on 02nd July at 5:00 am, as our flight was scheduled at 9:30 am IST at the Kempegowda International Airport (KIAL). After the 5 hour long journey, we landed in Changi Airport, Singapore at 5:00 pm local time.</p>

<p>Singapore, the World's costliest city, known for its clean and disciplined culture got us amazed at first sight. We were taken to various tourist attractions-<br>
Jurong Bird Park<br>
Sentosa Island<br>
Universal Studios<br>
Merlion<br>
	NEWater (water treatment plant)</p>

								<p align=justify>These were not merely attractions but everyplace we visited was systematic and disciplined. This helped us to understand and observe management functions in every aspect. We were given a fantastic accommodation & our stay at Singapore lasted 3 nights.</p>

								<p align=justify>Our next destination was Malaysia. We took the road transport to get there in just 5 hours. Malaysia is also known for its discipline and systematic way of life. Both countries have a lot of South Indian settlement, people, who were taken to work in the 1800's by the British Official languages are English, Mandarin & Tamil in both Singapore and Malaysia. The latter has a large Bangladeshi & Pakistani population who are primarily running small and medium businesses.</p>

<p>Malaysia has its own tourist attractions to boast about and also known for its tall buildings. Some of the attractions were-<br>
PETRONAS twin towers<br>
Istana Negara (The King's palace)<br>
Sunway Lagoon<br>
The Royal Selangoor<br>
Watch Emporium<br>
	Nasional Sports Stadium</p>

								<p align=justify>We left for Singapore after two nights in Malaysia by air and boarded the Singapore-Bangalore flight at 19:45 hours local time. We arrived at the KIAL, Bangalore at 21:45 hours on 09-July.</p>

								<p align=justify>Every place we visited had its own specialty and the learnings were equally good as the fun factor. We were able to experience how the International Cultures work and live. There are infinite memories and learnings we brought back home. This has helped us to discipline our lives and career in every aspect. We thank our College Management and the faculty in-charge for arranging this International Program so that every student got a glimpse of International Culture.</p>
