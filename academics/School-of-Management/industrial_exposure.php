								<p><b>Industrial Visits (2014 -2015)</b><br>
									<b>06.08.2014</b> - Unibic Biscuits India Pvt. Ltd, Bangalore</p>
								<p><b>INDUSTRIAL VISIT TO UNIBIC BISCUITS INDIA PVT. LTD.</b><br>
									<b>Date:</b> 06- Aug - 2014</p>
								<center><img src="images/unibic_visited.jpg"></center>

								<p align=justify>On the 6th August 2014, the department of management studies had organized an industrial visit for over 100 students, headed by two of the faculties of the department - Prof. Ragesh and Prof. Arul Senthilkumar. The I MBA students of 2014-2016 were thrilled to have gotten the opportunity for their very first Industrial visit which was to Unibic Biscuits India Pvt. Ltd., Heggadadevanapura Village, Huskur Road, Alur Po, Dasanapura Hobli, Bangalore North Taluk, (Near Nelmangala) Bangalore.</p>

								<p align=justify>Unibic has been manufacturing and marketing premium cookies in India since 2004 and operates as a subsidiary of Unibic Australia Pvt. Ltd. With Bangalore unit being the only unit in India. This Unit is equipped with the state-of-the-art machines from Italy. It is the first biscuit company to have put up a "wire cutting" technology which provides superior quality products and is the only method available to make real cookie. The Company has also installed equipment and packaging machinery for exports. The unit is capable of producing the around 30 tons products per day.</p>

								<p align=justify>Students were divided in group of 30 and were taken for the tour of the unit to understand the Manufacturing process. Firstly, they were taken to the Raw Material storing are where they were briefed about the raw materials required for the production. Next was the Pre-mixing segment where ingredients are pre- treated followed by Mixing them. Next, Forming Stage where cookies are made was explained. Baking is done at 220 C after which the cooled baked cookies are moved to Packaging segments where the highly efficient machines could pack around 120 packages per minute.</p>

								<p align=justify>HACCP (Hazard Analysis and Critical Control Point) certification has been done and recently they have obtained BRC (British Retailers Consortium) certification. Students were delighted to be informed that each time one buys a packet of Unibic ANZAC cookies, money is being raised to provide support to India's war widows, as well as the Indian JAWANS who risk their lives for us every day. UNIBIC donates 3% of sales for the support of Indian veterans.</p>

								<p align=justify>Students were amazed at the speed at which the process occurred and how the automated systems could work according to the requirements. The highly informative visit concluded on a sweet note as the students were offered complimentary cookies to munch on. The industrial visit to Unibic Biscuits India Pvt. Ltd. was an enriching experience for students in getting live exposure of manufacturing which can help them in their internships and further career enhancements.</p>
