<p align="justify">Talent Transformation Team, Centre for Employability and Corporate Relations (CECR) always bring out innovative initiatives which will increase the employability and help the students to select the right career choice.</p>

<p align="justify">Every year CECR kick starts the Campus Placements by Inaugurating GATE, Opening a Passage which is the Placement Season. Companies from different verticals visit our campus for Final Placements which include sharing the JD with CECR, Pre Placement Talk, Aptitude Test, GD, Technical Interviews and HR Interviews.</p>

<p align="justify">Over the years we have seen that there is a big gap when we do the recruitment process immediately after the PPT. The students are not in a position to take the right decision and they miss the clarity and focus. Only around 20 to 30% of the students have the clarity regarding the future career. This mismatch affects the Corporate 2 Campus and Campus 2 Corporate relationship. Finally there is a huge cost involved.</p>

<p align="justify">"UdyogVichaar" with a tagline C2C (Campus 2 Corporate / Corporate 2 Campus) is yet another initiative of the CECR to bridge the gap between C2C.</p>

<p><b style="color: #cd6e08;font-weight: bold;">About "UdyogVichaar"</b><br>
1. Most preferred Companies are invited to our Campus for PPT followed by Q & A<br>
2. Students are given a weeks' time to take a decision and to nominate for the role given<br>
3. The Excel Tracker and Profile is send to the Company ( If the number is more, the company can visit the Campus for Final Placements, If the number is less the company can call our students to their premises for further round)<br>
This will definitely help the Companies and College to have more clarity and will bridge the gap between C2C.</p>

<p align="justify">The inaugural session of UdyogVichaar was kick started with a premier company in IT and Consulting space 'UST Global'. The session took place on 4th August, 2016 at 11AM in the SKE Auditorium.</p>

<p align="justify">The Management Team, Dean - Sciences, Director - CECR & Coordinator- CECR along with the Faculties from the Computer Science Department were present for the Inaugural Ceremony.</p>

<p  align="justify">Prof. Neeta Dwivedi, Coordinator - CECR gave the Welcome address.<br>
The Presidential Address was delivered by Rev.Fr. Augustine George, Vice Principal and he urged students to be cautious about the Placement Season as it is a season of appointments and disappointments but the ultimate aim should be to get placed in life.</p>

<p>Prof. Sen B Mathews, Director - CECR gave an insight about Birth and Role of this new initiative.</p>

<p align="justify">The Chief Guest of the day was Mr Jaismon Emmauel, Senior Director, UST Global. His speech, very crisp & clear, touched on all areas of interest for a potential employee. He discussed on individual expectations, criteria for a job, priority of work field, career path and many more. He also spoke on the success of UST Global and its unique features-born digital, empowering people, educating people, touching as many lives as possible, educating people, and lastly, design for happiness.</p>

<p align="justify">Later the session was open to queries. This program had active student participation from various Departments - MCA, BCA, BSc CSME & CSMS who at the end of their scholastic life begin to have thoughts about employment.</p>

<p>"UdyogVichaar" thus, embarked upon a new chapter in CECR. It opened up the placement activities for the year 2017.</p>


