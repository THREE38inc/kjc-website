                                        <span>&nbsp;  Expert Lecture: Sustainable Development  <strong> through Corporate Social Responsibility</strong></span></h4>

									<p align=justify>The Department of Social work organised an expert lecture on the 6th March 2017 on the topic "Sustainable Development through Corporate Social Responsibility" for the MSW students.

									<p align=justify>The Resource person Mr. Dominic Thomas, (Associate Manager, Tech Mahindra Ltd. Bangalore) was felicitated by Dr. Jonas Richard A. (Head, Department of Social Work) during the welcome address.</p>

									<p align=justify>The session began on an interactive note by the speaker who helped the students to contemplate the concept and origin of the term CSR. Mr. Thomas implied on the difference between charity and philanthropy. Charity, as the speaker stated was aimed at doing good to the community by individuals whereas Philanthropy emerged after the World War II wherein different groups in the society came together to form charitable groups. Mr Thomas was emphatic about the fact that the concept of Social Work was not limited only to the Corporate, but extended to the individuals in the society at large. He added that every CSR strategy incorporated should be SMART (Specific, Measurable, Achievable, Reliable, and Time bound).</p>

									<p align=justify>The Resource Person discussed the various key areas and flagship programmes adopted by some of the corporate giants like TATA Power, Mahindra and Mahindra, L & T and Infosys. Some of their focus areas for CSR were Education of the socio- economically backward, youth, farmer's domain, community development, women empowerment, public health and safety. Mr Thomas elaborated on the strengths required by an effective CSR program like a road map strategy, good relation with the stakeholders and specific targets. Sir enlightened the students on how to make the CSR program sustainable by including activities which build on each other and to have a clear budget with matching funds, clear time frame for proper implementation and clear evaluation indicators.</p>

									<p align=justify>Mr. Thomas further discussed about the various programmes offered by Tech Mahindra as part of CSR, which comprised of ARISE (All Round Improvement in School Education) for children with disabilities, Shikshaanthar, SMART (Skills for Market Training and Employability Programme) and working with the APD (Association of People with Disabilities). Mr. Thomas, then mentioned about the direct intervention programmes and partnerships undertaken by Tech Mahindra like Tech Mahindra SMART Academy for Healthcare Services which has been setup in association with Harijan Sevak Sangh, a Gandhian Organisation.</p>

									<p align=justify>The expert lecture concluded with the question and answer session where the students raised their queries about various issues regarding the sustainability of the several CSR programmes and how they can be effectively implemented by partnering with the Government and the Non-Governmental agencies.</p>

									<p align=justify>The students expressed their gratitude to Mr. Dominic Thomas for his valuable insight and efforts. After which, Dr. Winnie Joyce A (Faculty, Department of Social Work, Kristu Jayanti College) presented the speaker with a memento as a token of appreciation</p>
									
