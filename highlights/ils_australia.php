									<p align=justify>On the 16th February 2017, as a part of the International Lecture Series, the Department of Social Work organised a Lecture on Social Work Licensing in Australia for the MSW students. The lecture aimed at educating the students on the scope of Social work and on the various requirements of a Social Worker to work in Australia.</p>

									<p align=justify>The expert speaker for the session was Mr. Jomet Mani (Managing Director, YESTE Migration Counseling, Australia) who was felicitated with a bouquet of flowers by Dr. Jonas Richard A. (Head, Department of Social Work, Kristu Jayanti College) during the formal Welcome.</p>

									<p align=justify>Mr. Jomet Mani being an alumni of the Department of Social Work of Kristu Jayanti College helped the students relate to the significance of the educational practices followed in the institution to boost career options for the students. Mr. Mani highlighted that field work and internship hours are significant and their successful completion could result in enriched practical knowledge and increased opportunity to work abroad. The expert speaker briefed the students on the migration procedure and the skill requirements of a Social Worker in Australia.</p>

									<p align=justify>Mr. Mani suggested that registration in the Australian professional bodies of Social Work like Australian Association of Social Work (AASW) and Australian Community Workers Association (ACWA) could guide aspiring Social Workers on migration to Australia. Further the various roles played by such professional bodies were discussed.</p>

									<p align=justify>The speaker then gave an overview of Social Work in Australia where, Mr. Mani emphasized that the target population is Child, Adult and Aged. Further, the various roles played by a Social Worker in Hospital, Community and Mental Health settings were elaborated by the speaker. Mr. Mani motivated the students to work hard apart from their academic requirements which would help students polish their skills and expand their knowledge in various settings of Social Work. The importance of identifying oneâ€™s interest in a specific target population like child mental health worker & adult mental health worker, etc. was highlighted by the speaker. The speaker concluded the session with a word of caution that students should work in organisations that credit their salaries to their bank account which is an added evidence required during the migration procedure to prove their work experience.</p>

									<p align=justify>The queries session included questions on lifestyle in Australia, challenges faced by Social Workers in Australia and application charges for various procedures of migration. The students felt that the time spent was worthwhile as they gained an understanding of Social Work in the Australian context which helped them to compare the Social Work systems followed in Australia and India.</p>

									<p align=justify>The students expressed their gratitude to Mr. Mani for his valuable insight and efforts. After which, Dr. I. Jerry Richards (Faculty, Department of Social Work, Kristu Jayanti College) presented the speaker with a memento as a token of appreciation</p>
									
