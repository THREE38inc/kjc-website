									<p><b>Objective:</b><br>
										The objective of this session was to be familiar with digital convergence for smart life. The sessions focused on topics relevant to both computer science and life science applications. The main aim of the workshop was to help the students to understand the importance of digitalization to make things simpler and easier. Convenience drives the force of thinking towards new inventions, and the resource persons were driving this thought process in the audience.</p>

									<p>The one day workshop on digital convergence for smart life was held on 16-01-2016 at 10:00am in the venue conference hall II .There were four sessions on digital convergence for smart life.</p>

									<p align=justify><b>FIRST SESSION</b><br>
TIME: 10:30 A.M to 11:30A.M.<br>
RESOURCE PERSON: MR.KISHORE ANNAM, PRINICIPAL CONSULTANT TELECOM, UNITED TELECOMS TECHNOLOGIES.<br>
TOPIC: WEARABLES AND DATA MINING<br>
										The lecture was about the wearable's and data mining his lecture covered the topics of machine learning terms, terminology of machine learning, machine language concepts in a nullshell,graph model used for in machine language, regression subset, chealth, mission of IEEE wearables such as sleep apnea device, inform doc daily, doctor reads data etc. The topics behind wearable's of IOT,IOT data quality and IOT, application domains .this first session helped us to gain knowledge about machine learning terms, wearable's ,data mining opportunities, veracity, wearables data interpretation and analytics cosmos and wearable's need edge computing. The conclusion was to take life with right architecture reference model (arm).</p>

									<p align=justify><b>SECOND SESSION</b><br>
TIME: 11:40 A.M to 12:40A.M.<br>
RESOURCE PERSON: DR.SYED SAIF FARBAR, IBM OPENPOWER,BANGALORE.<br>
TOPIC: IOT, IEEE AND ACADEMICS<br>
										This lecture was a briefing on the topic IOT, IEEE and academics. The topics covered during the session are history of IOT ,definition of IOT, ecosystem, smart solution to everyday issue, trending in IOT, impact of IOT, characteristics, difference between internet and iot, sample iot architecture,dna of iot,dna network characteristics,dna application languages,mission statement of IEEE IOT.The basic idea behind this lecture was to turn anything into a smart object and connect,monitor,manage,control,search etc without necessity for human intervention.The steps to turn an object into smart objects was also useful to understand the concept more.</p>

									<p align=justify><b>THIRD SESSION</b><br>
TIME: 2:00 P.M to 3:00P.M.<br>
RESOURCE PERSON: MS.JANAKI, PRINCIPAL TECHNICAL OFFICER, CDAC<br>
TOPIC: BIG DATA IN LIFE SCIENCE<br>
										This lecture covered the topics about big data analytics in life science and challenges involved in it. The objective of national supercomputing mission that is to built petaflop systems and hr training in parallel programming .The introduction to topic that with definition of genome, genome decoding using sequence, data mining algorithm are used to extract data from genome data, human genome annotated using computional, pharmacogenomics, adverse drug response ,multi dimensional approach for understanding disease mechanism, integrated data set for comparing and contrasting multiple tumor types, sources of large data repositories ,big data is the amount of data just beyond technology capacity to store, manage and process efficiently ,types of data, characteristics of big data, hace theorem, big data technologies, hadoop for genome analysis ,parallel implementation of blast, map reduce paradigm, use of spark. The overview of the lecture enriched the knowledge about genome and data extraction from genome and challenges involved it.</p>

									<p align=justify><b>FOURTH SESSION</b><br>
TIME: 03:00 P.M to 4:30A.M.<br>
RESOURCE PERSON: MR.NAVANEETHA KRISHNAN, SENIOR SCIENTIST, PHILIPS.<br>
TOPIC: BIG DATA IN HEALTH CARE<br>
										The session was about the big data in health .this session helped us to learn about the life science topic connected with the field of computer science the lecture had a briefing about health continuum, connected health, convergence of health technology, digital media and mobile telecommunication. Aims to put the correct information in the correct hands at the correct time moving towards a patient centered, outcome based delivery model, need for it, benefits and their examples, personal health, promoting health in low resource settings, features, challenges, research areas ,hr using mobile the conclusion for the topic was that the current care processes are insufficient to address the upcoming mismatch in supply to demand and connected health is no more important.</p>

									<p align=justify>The workshop on Digital convergence for smart life helped us to get a glimpse of the academics and the current trends in industry which would help us to improve and create the interest in learning current trends in industry .this workshop enriched us with knowledge on wearable's and data mining, internet of things, IEEE standards, big data concepts in life science and health care and challenges involved it .we thank the organisers for organising such a wonderful workshop which benefited us.</p>

