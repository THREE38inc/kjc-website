									<p><b>Title:</b> IEEE One Day Workshop on "Digital Convergence for Smart Life"<br>
Date: 16-01-2017<br>
Venue: MCA Conference Hall<br>
List of Resource Persons with details:<br>
Session I: Mr. Kishore Annam, Principal Consultant Telecom, United Telecoms Technologies Topic: Wearables and Data Mining<br>
Time: 10:30 am - 11:30 am<br>
Session II: Mr.Syed Saif Abrar, IBM OpenPower, Bangalore<br>
Topic: IoT, IEEE, Academics<br>
Time: 11:40 - 12:40<br>
Session III: Ms. Janaki, Principal Technical Officer, CDAC<br>
