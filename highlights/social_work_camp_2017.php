                                        <span>&nbsp;Social <strong>Work Camp 2017</strong></span></h4>

                                   <p align=justify>The Social Work Camp organized by the Department of Social Work, Kristu Jayanti College, Bangalore, in collaboration with Pastoral Sociology Institute (PSI), Malur, commenced on 30th of March 2017 and ended on 8th of April 2017. The camp was conducted at “Siddanahalli Village”, which is around 48 kilometres from Bangalore city. The village comes under Kolar District and is about 26 kilometres from Malur. It is known for its dairy farms and rose plantations. The major languages spoken are Kannada and Telugu</p>


									<p align=justify>The camp director was Dr. Jonas Richard A., and the student coordinators were guided by Dr. I. Jerry Richards, Dr. Winnie Joyce A. and Dr. Emmanuel Daniel. The camp preparations started with the pilot study by the students on 25th March, 2017. Dr. I. Jerry Richards accompanied the students and gathered information about the village.</p>

									<p align=justify>The campers began their journey on the 30th March afternoon along with the faculty members of the Department of Social Work. Rev. Fr. Riju Jose Vazhaparambil, Executive Director, PSI, Malur, helped the campers in arranging for their stay at the camp site. The camp began with the inaugural function on 31st March 2017 wherein the chief guest was Fr. Riju and the occasion was graced by Shri. Raghunath (President, Gram Panchayat) and Shri. Muniraju (Vice President, Gram Panchayat).</p>

									<p>Different themes were planned for each day. The following are the activities performed according to the respective themes.</p>

									<p align=justify>Waste Management: An awareness rally, research and data analysis and a cultural programme was held to sensitise the community as well as promote awareness on segregation of waste and the methods of waste management.</p>

									<p>Women Empowerment: The main aim of this theme was to make the people aware about the rights of women and ways to empower them through Self Help Groups.</p>

									<p align=justify>Youth Empowerment: The objective of this theme was to alert the youth of the community about the various opportunities provided by the Government through schemes and policies thereby helping them to be independent in the society.</p>

									<p>Awareness on Alcoholic Abuse: The community was made aware of the ill effects of alcoholism and the measures to prevent it.</p>

									<p align=justify>Sports Day: The campers were split into pairs and had visited the houses in the village in order to interact with the families and to get accustomed to the village lifestyle. Various games were conducted on the same day where participants of all age groups were involved.</p>

									<p align=justify>Prevention of Child Abuse: The campers took initiative to spread alertness and preventive measures to the community through rally, research and cultural programmes on the ongoing social evils against children.</p>

									<p align=justify>Environmental Protection: The main highlight of the day was the rally conducted by the campers where the significance of nature and ecosystem were emphasized. The campers were also taken on a visit to a vegetable plantation site within the village to understand the importance of agriculture.</p>

									<p align=justify>Cultural programmes based on the respective themes were held each day in the evening hours where the campers performed through skits, mimes, documentaries, songs, dances and theme based talks. The youth and children were also given opportunities to perform during the cultural programmes.</p>

									<p align=justify>Medical Camp: The Social Work campers in collaboration with St. Mary’s Hospital, Malur had conducted a medical camp for the community. The treatment was carried out by a group of medical practitioners followed by the free distribution of necessary medicines. The valedictory programme was held in the evening wherein the programme was graced by Fr. Riju in presence of the leaders of the village and the community. During this programme, the campers were also given the opportunity to share about their experiences and stay at Siddanahalli Village.</p>

									<p align=justify>The 8th of March was the final day of the camp. The main activities conducted on this day were sapling plantation and an evaluation on the entire camp in the presence of the President and Vice President of the Gram Panchayat. The departure began at 11:30 AM and the campers reached the college around 2:30 PM.</p>

									<p align=justify>The social work camp was a huge success as it provided the emerging social workers a lifelong opportunity to experience first-hand rural lifestyle as well as group living. The members of the community were hospitable and supportive because of which the campers had a comfortable stay throughout the camp. </p>

