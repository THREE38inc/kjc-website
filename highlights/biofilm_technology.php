                                        <span>&nbsp;  National Seminar On Biofilm Technology  <strong> – Problems & Prospects</strong></span></h4>

									<p align=justify>With every event that it organizes, more so in the Life sciences Department, Kristu Jayanti College never fails to use an opportunity to impress and educate. Yet again, this National Seminar on Biofilm Technology- Problems & Prospects has proved what this institution is capable of offering. The event flagged off with a total of 7 Universities, 7 Colleges with 54 delegates both from in and out of Karnataka, with the inaugural ceremony, where the presidential address was delivered by Honorable Principal Rev. Fr. Josekutty P.D.</p>

									<p align=justify>The chief guest address was delivered by Dr. Anand Khetkar- Associate director, Apotex pharmachem Pvt. Ltd., India, who set the pace of the National Seminar by bringing out the significance and economic importance of drug resistant Mycobacterium.</p>

									<p align=justify>This was followed by the technical sessions, the first session then began with the resource person being Dr S. Murugan- School of Biotechnology and Health Sciences, Karunya University, Coimbatore, Tamil Nadu, who spoke about "Relationship between adhesion and biofilm genes in clinical isolates of methicillin resistant and vancomycin- intermediate Staphylococcus aureus". Apart from the general description of biofilms, Dr. S. Murugan explained in detail, PIA biosynthesis, one of the causes of biofilm formation by S. aureus and it's various virulence genes, also focusing on taking the transdisciplinary approach in eliminating pathogenic bioflims.</p>

									<p align=justify>Following the first session was the session by Dr. Kushi Anand- Amit Singh Group, Centre for Infectious Disease Research (CIDR), Indian Institute of Science (IISC) Bangalore, Karnataka, who spoke about "Drug repurposing for tuberculosis by deconstructing drug resistance mechanisms”. He emphasized on the present scenario of prevalence of Tuberculosis in India and various aspects pertaining to the same.</p>

									<p align=justify>The next session was taken over by Dr. B. Anandkumar- Scientific Officer, Surface modification and characterization section, corrosion science and technology division, metallurgy and materials group, Indira Gandhi Centre for Atomic Research (IGCAR), Kalpakkam, Tamil Nadu, who spoke about "Non- conventional techniques to combat industrial biofilms/biofouling". He also emphasized on metal- microbial interaction, metal corrosion, biofouling and its control measures.</p>

									<p align=justify>The resource person for the fourth session was Dr. C. Nagaraj- Professor, PES Institute of Medical Sciences and Research (PESIMSR), Kuppam, Andhra Pradesh. Whose talk was about "Myths and Realities of biofilms in Medical Sciences." He took everyone through the history of biofilms thereby laying emphasis on the various roles played by biofilms from a medical perspective. Defining biofilm from a novel viewpoint as - Microbial cities of scientific significance, he accentuated the dual nature of biofilms- as a boon and as a bane.</p>

									<p align=justify>The speaker for the next session was Dr. Kavitha G- Scientist, Institute of Animal Health and Veterinary Biologicals, Bengaluru, who spoke about "Biofilms with special reference- Microbes of Veterinary Importance." She threw light on the formation of biofilm, microbes involved in the biofilm formation and also focused on the impact of biofilm formation.</p>

									<p>The paper session marked the end of the conference, a total of 6 posters were presented by students other college & universities.</p>

									<p align=justify>Therefore in a nutshell the national seminar on Biofilm Technology was truly successful in giving both the faculty members and the students an in depth knowledge about the various aspects in BiofilmTechnology and also provided an insight into the research aspect as well.</p>

									   

