                                        <span>&nbsp;Workshop on    <strong> Gender Sensitivity</strong></span></h4>

									<p align=justify>The Department of Social Work organized a two day workshop on “Gender Sensitivity” for the 2nd year MSW students on the 24th & 25th March 2017. The resource persons Ms. Asha V (Co-ordinator Rural Women’s Collective & Bandhavi, Visthar) & Ms. Sagaya Shanthi (Program Co-ordinator, Visthar) were felicitated by Dr. Jonas Richard (Head, Department of Social Work).</p>

<p align=justify>The first day session began with the trainers orienting the students through group activities & discussions to enable students to derive the differences between the basic concepts of Gender Sensitivity namely; sex and gender, gender equality and equity & gender discrimination and gender biased discrimination. The sessions gave a broadened perspective on the challenges faced by trainers in incorporating gender sensitization. The trainers used real life examples of women who have been successful in most aspects of life to shed light on the fact that men and women are physiologically different and that the roles that they play in the society are socially constructed. Through the activities, the trainers aimed at enabling the students to question the current status of gender in the society, consequently imparting an understanding on Gender Sensitivity.

									<p align=justify>The second day of the Workshop began with a recap of the previous sessions. Further, the students were divided into four groups and were directed by the trainers to present a role play on gender discrimination in various walks of life. The core areas depicted by the four groups were gender discrimination in the family, gender discrimination in the community, gender discrimination in media and gender discrimination in education. The role plays enacted by the students helped them realize the dire need for gender sensitization in the present scenario.</p>

									<p align=justify>Stories that conveyed the ground realities of gender discrimination were narrated by the trainers. This gave a bird’s eye view on discrimination against women being a socially acceptable behavior. Further, a documentary named “Killing us Softly” was viewed by the students. The documentary was true eye opener on how advertisements promote gender discrimination.</p>

									<p align=justify>The workshop concluded with a valedictory session where the students thanked the trainers for their efforts in stirring gender sensitivity among the participants. The students expressed their gratitude to the trainers as the workshop helped them express their views and exchange ideas with their fellow mates. A few students gave their feedback on the workshop wherein they stated that the workshop has widened & strengthened their understanding of Gender Sensitivity and that they aspire to become trainers for the same. </p>
									
