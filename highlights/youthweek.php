                                        <span>&nbsp; Centre for Social Activities  <strong> NATIONAL YOUTH WEEK</strong></span></h4>

									<p align=justify>Centre for Social Activities has organized “ National Youth Week” from 12th to 21st January 2017 to commemorate the principles ,values and ideologies of Swami Vivekananda . As part of National Youth Week various event were organized in association with Srishti Foundation Bangalore, Lions Blood Bank, TTK Blood Bank, NIMHANS Hospital, Apollo Hospital Bangalore, Political Science department, Psychology department and Life Science department of Kristu Jayanti College. The youth week aims to propagate the concept of national integration, spirit of communal harmony, Leadership and courage amongst the youth. The activities held during the youth week include Video Screening, Guest Lecture, Blood Donation camp, Cultural events, Debate, First Aid Training, Seminars and Yoga.</p>

									<p align=justify>12th January 2017, first day of youth week was scheduled with a video screening of Swami Vivekananda. The video projected various incidents of his life and his teachings. The projection of his life became inspirational to the volunteers.</p>

									<p align=justify>13th January 2017, second day of the youth week was witnessed with a guest lecture on women harassment and self-control by Dr.Rajalakshmi, Srishti Foundation, Bangalore, an organization working on child safety. She spoke about the main challenges in doing social service. She also taught about the generation of emotions and the ways to control it.</p>

									<p align=justify>16th January 2017, third day of youth week was scheduled with a guest lecture on the principles of Swami Vivekananda and Leadership by the powerful and energetic Prof. Shreshta Goush Chowdary, Department of Political Science, Kristu Jayanti College. The session was very interactive as volunteers asked various questions such as involvement of India in UNO, Swami Vivekananda’s sadhu life.</p>

									<p align=justify>17th January 2017, four day witnessed Blood Donation Camp in association with NSS, NCC, MSW department, Lions Blood Bank, TTK Rotary Blood bank and Nimhans Blood Bank. 465 units of blood were collected by the blood banks. Various students and staff of KJC donated blood. Student’s actively and enthusiastically participated in donating blood.</p>

									<p align=justify>Day four also witnessed the presentation on Great Leaders by CSA volunteers. There were presentions by Demetrius, Solomon, Jeen, Kavya, Feba and group, Suchitra and group and Sarang and group. The presentations were on John D. Rockefeller, St. Kuriakose Alias Chavara, Nelson Mandela, APJ Abdul Kalam and Malala Yousafzai. All these presentation filled enthusiasm and inspiration in volunteers.</p>

								    <p align=justify>18th January 2017, on the fifth day of the youth week, a guest lecture on food and health was organized. Dr. Sonia Angeline, Department of Life Science, Kristu Jayanti College delivered the lecture on health, hygiene and nutrition. She enriched students with various steps to stay healthy. Some of the steps includes consuming plenty of water, avoiding junk foods, getting enough sleep and proper exercise.</p>

									<p align=justify>19th January 2017, on the sixth day of the youth week, a guest lecture on Drug and Youth was presented by Prof. Smitha A.G., Department of Psychology, Kristu Jayanti College. She conveyed very useful message to the present youth and explained the stages of substance abuse which includes use, abuse, addiction and dependence. Mam began her lecture on smoking, it effects and measures.</p>

									<p align=justify>20th January 2017, seventh day of the youth week CSA provided a platform for all volunteers to showcase their talents. The event witnessed with talents like Debate, Mimicry, Solo and group dances, a beautiful poem, patriotic song and fashion show.</p>

									<p align=justify>21st January 2017, eighth day Yoga was organized for K. Narayanapura Government School students from 8:00am to 9:00am. Yoga is the art work of awareness on the canvas of body, mind and soul. You cannot always control what goes on outside. But you can always control what goes on outside with the help of yoga. It increases the concentration power. Under the initiative of few volunteers</p>

									<p align=justify>First Aid Training was organized on 21st January 2017 in association with Apollo hospital, Bangalore. The event happened in mini audi-1 from 10 am to 12.30 pm. Dr. Sai Vishnu, Dr. Akash Verma and Dr. Swapnil from Apollo hospital conducted the program. Their presentation was on first aid measures related to Fracture, Bleeding, heart attack, snake bite, unconscious etc.. They trained volunteers with demonstration which helped the volunteers to understand the steps further deeply. There was active participation from volunteers. The youth week came to an end inculcating volunteers with many experiences, knowledge, do’s and don’ts, values and principles.</p>
									
